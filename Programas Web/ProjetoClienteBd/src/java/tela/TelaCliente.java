    private void bSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvaActionPerformed
        bd = new BdCliente();
        telaToCliente();
        if (novo) {
            bd.insere(cliente);
        } else {
            bd.insere(cliente);
        }
    }//GEN-LAST:event_bSalvaActionPerformed

    private void bExcluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluiActionPerformed
        bd = new BdCliente();
        bd.exclui(Integer.parseInt(tCodigo.getText()));
        limpaTela();
        novo = true;
    }//GEN-LAST:event_bExcluiActionPerformed

    private void bLocalizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLocalizaActionPerformed
        bd = new BdCliente();
        String busca = JOptionPane.showInputDialog("Digite o código do cliente a buscar");
        cliente = bd.localiza(Integer.parseInt(busca));
        if (!cliente.getNome().equals("")) {
            clienteToTela();
            novo = false;
        } else {
            JOptionPane.showMessageDialog(this, "Usuário não existente!");
            limpaTela();
            novo = true;
        }
    }//GEN-LAST:event_bLocalizaActionPerformed

